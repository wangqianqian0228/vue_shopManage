<template>
  <div class="user-box">
    <!-- 面包屑导航栏 -->
    <el-breadcrumb separator-class="el-icon-arrow-right">
      <el-breadcrumb-item :to="{ path: '/' }">首页</el-breadcrumb-item>
      <el-breadcrumb-item>用户管理</el-breadcrumb-item>
      <el-breadcrumb-item>用户列表</el-breadcrumb-item>
    </el-breadcrumb>

    <!-- 卡片 -->
    <el-card class="box-card">
      <div slot="header" class="clearfix">
        <el-row :gutter="30">
          <el-col :span="14"
            ><div class="grid-content bg-purple">
              <el-input placeholder="请输入内容" class="input-with-select" v-model='queryInfo.query'>
                <el-button slot="append" icon="el-icon-search" @click='getUserList'></el-button>
                <!-- 只要点击了这个搜索按钮，就去执行getUserList这个函数，这个时候input输入框已经与queryInfo.query进行了双向数据绑定 -->
              </el-input></div
          ></el-col>
          <el-col :span="10"
            ><div class="grid-content bg-purple">
              <el-button type="primary">添加用户</el-button>
            </div></el-col
          >
        </el-row>
      </div>

      <!-- 列表区域 -->
      <el-table :data="userLists" style="width: 100%">
        <el-table-column type="index"></el-table-column>
        <!-- 添加索引列 -->
        <el-table-column prop="id" label="ID" width="120"> </el-table-column>

        <el-table-column prop="username" label="用户名" width="120">
        </el-table-column>
        <el-table-column prop="role_name" label="角色" width="120">
        </el-table-column>

        <el-table-column prop="email" label="邮箱" width="200">
        </el-table-column>
        <el-table-column prop="mobile" label="电话"> </el-table-column>
        <el-table-column label="创建日期">
          <template slot-scope="scope">
            {{ scope.row.create_time | dateString }}
            <!-- {{scope.row}} -->
          </template>

          <!-- 作用域插槽 :不是直接插入数据，而是对数据进行相关处理的时候
          将通过 scope.row.字段名 来获取相对应的属性值，会覆盖prop对应的值-->

          <!-- 在相应的el-table-column下面插入一个 template，其中
          参数scope是一个对象，来接收当前作用域的数据，scope.row可以输出相应的这一列的信息（对象）
          { "id": 500, "role_name": "超级管理员", "username": "admin", "create_time": 1486720211, "mobile": "12345678", "email": "adsfad@qq.com", "mg_state": true }
          -->
        </el-table-column>
        <el-table-column label="状态">
          <template slot-scope="scope">
            <!-- {{scope.row.mg_state}} -->
            <!-- change函数什么时候触发？
            switch 状态发生变化时的回调函数 -->
            <el-switch
              v-model="scope.row.mg_state"
              @change="changeState(scope.row)"
            >
            </el-switch>
          </template>
        </el-table-column>
        <el-table-column fixed="right" label="操作">
          <template slot-scope="scope">
            <el-button @click="handleClick(scope.row)" type="text" size="small"
              >查看</el-button
            >
            <el-button type="text" size="small">编辑</el-button>
            <el-button type="text" size="small">删除</el-button>
          </template>
        </el-table-column>
      </el-table>

      <!-- 分页区域 -->
      <el-pagination
        @size-change="handleSizeChange"
        @current-change="handleCurrentChange"
        :current-page="queryInfo.pagenum"
        :page-sizes="[1, 2, 5, 10]"
        :page-size="queryInfo.pagesize"
        layout="total, sizes, prev, pager, next, jumper"
        :total="total"
      >
        <!--  // 根据这个pagesize来分页1 2 3 4  -->
      </el-pagination>
    </el-card>
  </div>
</template>

<script>
export default {
  data() {
    return {
      // 获取用户列表的参数对象
      queryInfo: {
        //   查询实现原理：input输入框中的数据与query进行双向数据绑定，然后点击搜索按钮再去后台获取数据。
        query: "",
        // 当前的页数
        pagenum: 1,
        // 每页显示多少条数据
        pagesize: 10,
        // 根据这个pagesize来分页1 2 3 4
      },
      userLists: [],
      total: 0,
    };
  },
  created() {
    this.getUserList();
  },
  components: {},

  computed: {},

  mounted() {},

  methods: {
    async getUserList() {
      const { data: res } = await this.$http.get("users", {
        params: this.queryInfo,
      });
      //   console.log(res.data);
      if (res.meta.status !== 200) {
        this.$message.error(`${res.meta.msg}`);
      }
      this.userLists = res.data.users;
      //   console.log(this.userLists);
      //   总共有多少条数据
      this.total = res.data.total;
    },
    // 监听页面每页显示多少条的改变的函数
    handleSizeChange(val) {
      //   console.log(`每页 ${val} 条`);
      // 每页显示多少条数据
      this.queryInfo.pagesize = val;
      //   再去发送请求数据
      this.getUserList();
    },
    // 监听当前页码值改变的函数
    handleCurrentChange(val) {
      //   console.log(`当前页: ${val}`);
      this.queryInfo.pagenum = val;
      this.getUserList();
    },
    async changeState(newState) {
      console.log(newState);
      // 这一行的数据的具体信息
      // { "id": 500, "role_name": "超级管理员", "username": "admin", "create_time": 1486720211, "mobile": "12345678", "email": "adsfad@qq.com", "mg_state": true }

      // 运用put方法直接修改了后台数据库的数据
      const { data: res } = await this.$http.put(
        `users/${newState.id}/state/${newState.mg_state}`
      );
      if (res.meta.status !== 200) {
        // 修改失败，则把状态改回来
        newState.mg_state = !newState.mg_state;
        return this.$message.error(res.meta.msg);
      }
      this.$message.success(res.meta.msg);
    },
  },
};
</script>
